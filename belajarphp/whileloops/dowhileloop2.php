<?php
//do while loop akan selaluu mengeksekusi blok kode sekali, maka ia akan memeriksa kondisi,dan ulangi loop selama kondisi yang selama
$x=6;

do{
    echo"The number is: $x <br>";
    $x++;
} while ($x <= 5); //untuk kasus ini,outputnya adalah 6 sebab do while akan mengeksekusi pernyataan setidaknya sekali meskipun salah sebab kondisi diuji setelah mengksekusi laporan.
?>