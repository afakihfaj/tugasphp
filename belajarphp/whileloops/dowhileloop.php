<?php
//do while loop akan selaluu mengeksekusi blok kode sekali, maka ia akan memeriksa kondisi,dan ulangi loop selama kondisi yang selama
$x=1;

do{
    echo"The number is: $x <br>";
    $x++;
} while ($x <= 5); //selama value dari variable x kurang dari atau sama dengan 5,maka value variable x akan terus bertambah sampai 5
?>